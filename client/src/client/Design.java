    public void saveUser(String user) {
        File f = new File("/user");
        if (!f.isDirectory()) {
            f.mkdir();
        }
        f = new File("/user/user.db");
        try {
            f.createNewFile();
            FileWriter fw = new FileWriter(f.getAbsolutePath());
            fw.write(user);
            fw.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
