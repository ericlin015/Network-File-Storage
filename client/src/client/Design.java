    public boolean correctChar(String name) {
        System.out.println("Checking character.");
        if (name.contains("\r\n")) {
            return false;
        } else if (name.contains("/")) {
            return false;
        } else if (name.contains("\\")) {
            return false;
        } else if (name.contains(":")) {
            return false;
        } else if (name.contains("*")) {
            return false;
        } else if (name.contains("?")) {
            return false;
        } else if (name.contains("*")) {
            return false;
        } else if (name.contains("\"")) {
            return false;
        } else if (name.contains("<")) {
            return false;
        } else if (name.contains(">")) {
            return false;
        } else if (name.contains("|")) {
            return false;
        } else if (name.contains(" ")) {
            System.out.println("name has spaces.");
            return false;
        }
        return true;
    }
